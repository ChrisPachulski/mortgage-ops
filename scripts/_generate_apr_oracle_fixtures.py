def _build_request_payload(
    *,
    principal: str,
    annual_rate: str,
    term_months: int,
    loan_type: str,
    finance_charges: str,
    payment_amount: str,
    odd_first_period_days: int = 0,
) -> dict[str, Any]:
    """Assemble the JSON-string-typed APRRequest payload.

    Strict mode requires Decimal/date inputs to be JSON-serializable strings
    and validated via APRRequest.model_validate_json (matching the CLI body).
    """
    amount_financed = (Decimal(principal) - Decimal(finance_charges)).quantize(Decimal("0.01"))
    return {
        "loan": {
            "principal": principal,
            "annual_rate": annual_rate,
            "term_months": term_months,
            "origination_date": "2026-01-01",
            "loan_type": loan_type,
        },
        "finance_charges": finance_charges,
        "advance_schedule": [
            {"unit_period_offset": 0, "amount": str(amount_financed)},
        ],
        "payment_schedule": [
            {
                "starting_unit_period": 1,
                "periods": term_months,
                "amount": payment_amount,
            },
        ],
        "day_count": "30/360",
        "unit_periods_per_year": 12,
        "odd_first_period_days": odd_first_period_days,
    }


def _solve(payload: dict[str, Any]) -> tuple[Decimal, int, Decimal]:
    """Run solve_apr against the payload; return (apr, iterations, residual)."""
    request = APRRequest.model_validate_json(json.dumps(payload))
    response = solve_apr(request)
    return response.estimated_apr, response.iterations, response.final_residual


def _serialize(
    *,
    fixture_id: str,
    description: str,
    payload: dict[str, Any],
    provenance_class: str,
    provenance_notes: str,
    cross_validated_against: str | None = None,
) -> dict[str, Any]:
    """Run the engine and assemble the fixture dict (engine-emitted expected)."""
    apr, iterations, residual = _solve(payload)
    fixture: dict[str, Any] = {
        "description": description,
        "request": payload,
        "expected": {
            "estimated_apr": str(apr),
            "iterations_max": max(iterations + 5, 10),  # generous headroom
            "tolerance_used": "0.00001",
            "iterations_observed_at_capture": iterations,
            "final_residual_at_capture": str(residual),
        },
        "oracle_provenance": {
            "class": provenance_class,
            "captured_at": CAPTURED_AT,
            "captured_by": "scripts/_generate_apr_oracle_fixtures.py",
            "engine_module": "lib.apr.solve_apr",
            "notes": provenance_notes,
        },
    }
    if cross_validated_against:
        fixture["oracle_provenance"]["cross_validated_against"] = cross_validated_against
    return fixture


