def main() -> int:
    ORACLE_DIR.mkdir(parents=True, exist_ok=True)
    specs = fixture_specs()
    if len(specs) < 20:
        print(f"FATAL: corpus has {len(specs)} specs; need >= 20", file=sys.stderr)
        return 1

    summary: list[tuple[str, str, int, str]] = []
    for spec in specs:
        payload = _build_request_payload(**spec["kwargs"])
        fixture = _serialize(
            fixture_id=spec["id"],
            description=spec["description"],
            payload=payload,
            provenance_class=spec["provenance_class"],
            provenance_notes=spec["provenance_notes"],
            cross_validated_against=spec.get("cross_validated_against"),
        )
        out_path = ORACLE_DIR / f"{spec['id']}.json"
        out_path.write_text(json.dumps(fixture, indent=2) + "\n")
        apr_str = fixture["expected"]["estimated_apr"]
        iters = fixture["expected"]["iterations_observed_at_capture"]
        provenance_short = (
            "regulatory"
            if spec["provenance_class"].startswith("regulatory")
            else "engine-emitted+xval"
            if "cross-validated" in spec["provenance_class"]
            else "engine-emitted"
        )
        summary.append((spec["id"], apr_str, iters, provenance_short))
        print(f"WROTE {out_path.name:50s}  APR={apr_str}  iters={iters}  [{provenance_short}]")

    print(f"\nGenerated {len(specs)} fixtures at {ORACLE_DIR}")
    print(f"Capture date: {CAPTURED_AT}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
